local djb2 = require "dromozoa.commons.hash.djb2"
assert(djb2.string("", 5381) == 5381)
assert(djb2.string("a", 5381) == 177670)
assert(djb2.string("ab", 5381) == 5863208)
assert(djb2.string("abc", 5381) == 193485963)
assert(djb2.string("abcd", 5381) == 2090069583)
assert(djb2.string("abcde", 5381) == 252819604)
assert(djb2.string("abcdef", 5381) == 4048079738)
assert(djb2.string("abcdefg", 5381) == 442645281)
assert(djb2.string("abcdefgh", 5381) == 1722392489)
assert(djb2.string("abcdefghi", 5381) == 1004377394)
assert(djb2.uint32(0, 5381) == 2086473605)
assert(djb2.uint32(1, 5381) == 2086509542)
assert(djb2.uint64(0, 5381) == 2075347461)
assert(djb2.uint64(1, 5381) == 1744117478)
assert(djb2.double(0, 5381) == 2075347461)
assert(djb2.double(1, 5381) == 2075355444)
